    private void valorDigitado( String caractere ){
        if ( Visor.getText().equals("") ){
            Visor.setText(caractere);// Seto para o valor inserido
        } else {
            Visor.setText(Visor.getText().concat(caractere));// Concateno o que tenho com o que veio
        }
    }
    
    private Integer converteEmInteiro( String numero ){
        NumberFormat n = NumberFormat.getInstance();
        Integer d = 0;
        
        try {
            d = n.parse(numero).intValue();
        } catch ( ParseException ex ) {
            
        }
        return d;
    }
    
    private String converteEmString ( Integer numero ){
        if ( numero != null ){
            DecimalFormat formato = new DecimalFormat("##########", new DecimalFormatSymbols(new Locale("pt","BR")));
            formato.setParseBigDecimal(true);
            return formato.format(numero);
        }
        return "";
    }
    
